"""Константы для валидации версий приложения."""

# Регулярное выражение для проверки формата версии.
# Проверяет, что версия соответствует формату YYYY.MM.PATCH, где:
# - YYYY - четырехзначный год (например, 2023, 2024)
# - MM - номер месяца от 01 до 12
# - PATCH - номер патча (целое число)
# Примеры корректных значений: "2023.12.1", "2024.01.5"
VERSION_REG_EXP: str = r"^\\d{4}\\.\\d{1,2}\\.\\d+$"
# Минимально допустимый год в версии приложения.
# Определяет нижнюю границу допустимых значений года.
VERSION_MIN_YEAR: int = 2025
# Минимально допустимое значение месяца в версии приложения.
MIN_VERSION_MONTH: int = 1
# Максимально допустимое значение месяца в версии приложения.
MAX_VERSION_MONTH: int = 12
